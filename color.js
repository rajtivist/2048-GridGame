export function colorsetter(gindex,arrelement,button)
    {
        switch(arrelement)
        {
          

            case 2:
                button[gindex].style.backgroundColor="#C28170";
                break;

            case 4:
                button[gindex].style.backgroundColor="#A3938F";
                break;

            case 8:
                button[gindex].style.backgroundColor="#e88d4a";
                break;
                
            case 16:
                button[gindex].style.backgroundColor="#e84a4e";
                break;
                
            case 32:
                button[gindex].style.backgroundColor="#e76a4a";
                break;


            case 64:
                button[gindex].style.backgroundColor="#c8974a";
                break;


            case 128:
                button[gindex].style.backgroundColor="#afbb4a";
                break;


            case 256:
                button[gindex].style.backgroundColor="#FF5C33";
                break;


            case 512:
                button[gindex].style.backgroundColor="#E76A4A";
                break;


            case 1024:
                button[gindex].style.backgroundColor="#CC7A66";
                break;


            case 2048:
                button[gindex].style.backgroundColor="#F31F1B";
                break;


        }
    }
    








